def build_toc(patterns):
    groups = {}
    for i, p in enumerate(patterns):
        g = p["group"]
        if g not in groups:
            groups[g] = []
        groups[g].append((i + 1, p))

    html = ""
    for g, items in groups.items():
        html += f'<div class="toc-group">'
        html += f'<div class="toc-group-name">{g}</div>'
        for num, p in items:
            html += (
                f'<div class="toc-item">'
                f'<span class="num">No.{num:02d}</span>'
                f'<span class="pname">{p["name"]}</span>'
                f'<span class="stars-small">{stars(p["rating"])}</span>'
                f'</div>'
            )
        html += '</div>'
    return html


